package lastTest.CommandPattern.lightControl;

public class GarageDoor {
    public void up(){
        System.out.println("the door is open to up");
    }
    public void down(){
        System.out.println("the door is close to down");
    }
    public void stop(){
        System.out.println("stop openning the door");
    }
    public void lightOn(){
        System.out.println("turn on the light");
    }
    public void lightOff(){
        System.out.println("turn off the light");
    }

}
